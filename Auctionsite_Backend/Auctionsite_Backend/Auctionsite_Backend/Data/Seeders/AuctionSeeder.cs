        public static async Task CreateAuctions(AuctionSiteDbContext dbContext)
        {
            if (await dbContext.Auctions.AnyAsync()) return;

            var auctions = SeedAuctions.Select(a => new Auction
            {
                Title = a.title,
                Description = a.description,
                AskingPrice = a.askingPrice,
                StartDateTime = a.startDateTime,
                EndDateTime = a.endDateTime,
                UserId = a.userId,
                ImageUrl = a.imageUrl,
                CreatedAt = a.createdAt,
                EditedAt = a.editedAt,
                IsOpen = a.isOpen,
                IsActive = a.isActive,
            });

            await dbContext.Auctions.AddRangeAsync(auctions);
            await dbContext.SaveChangesAsync();
        }
    }
